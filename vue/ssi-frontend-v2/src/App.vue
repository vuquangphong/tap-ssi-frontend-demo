<template>
  <router-view />
</template>

<script>
import { LOCAL_STORAGE_TOKEN_NAME } from "./utils/constants";
export default {
  mounted() {
    if (this.$route.path === "/") {
      if (localStorage.getItem(LOCAL_STORAGE_TOKEN_NAME)) {
        this.$router.push("/home");
      } else {
        this.$router.push("/login");
      }
    }
  },
};
</script>

